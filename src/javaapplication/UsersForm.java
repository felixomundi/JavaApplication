    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
       //get name and email entered by user
       String name = txtName.getText();
       String email = txtEmail.getText();
       String ID = txtId.getText();
       int national = Integer.parseInt(ID);
       
       try{
           Database db = new Database();
           con = db.dbConnect();
           PreparedStatement pstmt = con.prepareStatement("INSERT INTO users (name, email, national) VALUES (?, ?, ?)");
           pstmt.setString(1,name);
           pstmt.setString(2, email);
           pstmt.setInt(3, national);
           pstmt.executeUpdate();
           JOptionPane.showMessageDialog(this,"Student Record Inserted");
           txtEmail.setText("");
           txtName.setText("");
           txtId.setText("");
           
       
       }
       catch (SQLException ex) {
            Logger.getLogger(Database.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try{
           Database db = new Database();
           con = db.dbConnect();
           PreparedStatement pstmt = con.prepareStatement("select * from users where email = ?");
           pstmt.setString(1,txtEmail.getText());  
           ResultSet rs = pstmt.executeQuery();
           if(rs.next()){
           txtEmail.setText(rs.getString("email"));
           txtName.setText(rs.getString("name"));
           txtName.setText(rs.getString("name"));
           txtId.setText(rs.getString("national"));           
           }
           else{
            JOptionPane.showMessageDialog(this,"Record Not Found");
           }
        }
         catch (SQLException ex) {
            Logger.getLogger(Database.class.getName()).log(Level.SEVERE, null, ex);
        
        }
    }//GEN-LAST:event_btnSearchActionPerformed

