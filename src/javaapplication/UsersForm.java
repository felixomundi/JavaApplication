    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String ID = txtId.getText();
        int national = Integer.parseInt(ID);
         try{
           Database db = new Database();
           con = db.dbConnect();
           PreparedStatement pstmt = con.prepareStatement("update users set email=?, name=?, national=? where email = ?");
           pstmt.setString(1,txtEmail.getText());
           pstmt.setString(2,txtName.getText());
           pstmt.setInt(3,national);
           pstmt.setString(4,txtEmail.getText());           
           pstmt.executeUpdate();
           txtEmail.setText("");
           txtName.setText("");
           txtId.setText("");
           con.close();
           JOptionPane.showMessageDialog(this,"Updated Successfully");

        }
         catch (SQLException ex) {
            Logger.getLogger(Database.class.getName()).log(Level.SEVERE, null, ex);
        
        }
        
        
    }//GEN-LAST:event_updateButtonActionPerformed

